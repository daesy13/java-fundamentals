function square(number){
	return number*number
}

function fullName(firstName, lastName){
	return firstName + " " + lastName
}

function average(num1, num2){
	return (num1+num2)/2
}

function greeter(name){
	return "Hello " + name + "!"
}

function areaOfSquare(s){
	return s*s
}

function perSquare(s){
	return 4*s
}

function areaOfRect(w,l){
	return w*l
}

function perRect(w,l){
	return 2*(l+w)
}

function areaParallelogram(l,h){
	return l*h
}

function areaTrapezoid(h,b1,b2){
	return ((b1+b2)*h)/2
}

function areaTriangle(b,h){
	return (b*h)/2;
}

function circle(r){
	return Math.PI*r*r;
}

function futureValue(p,i,n,t){
	return p * Math.pow((1+(i/n)), n*t);
}

