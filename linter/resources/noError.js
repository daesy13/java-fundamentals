function square(number){
	return number*number;
}

function cube(x){
	return x*x*x;
}

function fullName(firstName, lastName){
	return firstName + " " + lastName;
}

function average(num1, num2){
	return (num1+num2)/2;
}

function greeter(name){
	return "Hello " + name + "!";
}

function areaOfSquare(s){
	return s*s;
}

function perSquare(s){
	return 4*s;
}
